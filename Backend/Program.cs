var app = builder.Build();

app.UseCors("AllowAll");
// app.UseHttpsRedirection();
// app.UseAuthentication();
app.UseAuthorization();

if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.MapControllers();
app.Run();
